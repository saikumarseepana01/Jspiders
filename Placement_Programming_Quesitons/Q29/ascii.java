package Q29;

public class ascii {
    public static void main(String[] args) {
        //print ascii values of alphabets from a to z;
        for (char i = 'a'; i <= 'z'; i++) {
            System.out.println("ASCII value of " + i + " is " + (int) i);
        }
        //print ascii values of alphabets from A to Z;
        for (char i = 'A'; i <= 'Z'; i++) {
            System.out.println("ASCII value of " + i + " is " + (int) i);
        }
    }
}
